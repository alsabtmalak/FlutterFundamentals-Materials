/* Example 2.9 - Math Operators in Dart */

void main() {
  int a = 5;
  int b = 2;

  print('Addition: ${a + b}');          // 7
  print('Subtraction: ${a - b}');       // 3
  print('Multiplication: ${a * b}');    // 10
  print('Division: ${a / b}');          // 2.5
  print('Integer Division: ${a ~/ b}'); // 2
  print('Modulus: ${a % b}');           // 1
}